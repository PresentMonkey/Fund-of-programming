#include <iostream>

using namespace std;

int main(){
    cout << "Char has " << sizeof(char) << " bytes" << endl;
    cout << "Int has " << sizeof(int) << " bytes" << endl;
    cout << "Float has " << sizeof(float) << " bytes" << endl;
    cout << "Double has " << sizeof(double) << " bytes" << endl;
    return 0;
}